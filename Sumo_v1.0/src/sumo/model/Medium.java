public class Medium {
	private long id;
	private String titel;
	private int menge;
	private String bildpfad;
	private String genre;
	private int ausleihungen;
	private float preis;
	private Date erwerbsdatum;
	private short typ;
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getTitel() {
		return titel;
	}
	public void setTitel(String titel) {
		this.titel = titel;
	}
	public int getMenge() {
		return menge;
	}
	public void setMenge(int menge) {
		this.menge = menge;
	}
	public String getBildpfad() {
		return bildpfad;
	}
	public void setBildpfad(String bildpfad) {
		this.bildpfad = bildpfad;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public int getAusleihungen() {
		return ausleihungen;
	}
	public void setAusleihungen(int ausleihungen) {
		this.ausleihungen = ausleihungen;
	}
	public float getPreis() {
		return preis;
	}
	public void setPreis(float preis) {
		this.preis = preis;
	}
	public Date getErwerbsdatum() {
		return erwerbsdatum;
	}
	public void setErwerbsdatum(Date erwerbsdatum) {
		this.erwerbsdatum = erwerbsdatum;
	}
	public short getTyp() {
		return typ;
	}
	public void setTyp(short typ) {
		this.typ = typ;
	}
	
	
}//#Medium
