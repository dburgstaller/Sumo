package sumo.model;

import java.sql.Date;
import java.sql.Timestamp;

public class Mitarbeiterstunde {
	private long id;
	private Mitarbeiter mitarbeiter;
	private Timestamp angekommen;
	private Timestamp gegangen;
	private String bemerkung;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public Mitarbeiter getMitarbeiter() {
		return mitarbeiter;
	}
	public void setMitarbeiter(Mitarbeiter mitarbeiter) {
		this.mitarbeiter = mitarbeiter;
	}
	public Timestamp getAngekommen() {
		return angekommen;
	}
	public void setAngekommen(Timestamp angekommen) {
		this.angekommen = angekommen;
	}
	public Timestamp getGegangen() {
		return gegangen;
	}
	public void setGegangen(Timestamp gegangen) {
		this.gegangen = gegangen;
	}
	public String getBemerkung() {
		return bemerkung;
	}
	public void setBemerkung(String bemerkung) {
		this.bemerkung = bemerkung;
	}
	
}
